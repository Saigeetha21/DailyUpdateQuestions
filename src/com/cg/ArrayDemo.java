package com.cg;

public class ArrayDemo {
	public void printArray(int[] a) {
		for (int i=0;i<a.length;i++) {
			System.out.println(" "+a[i]);
		}
	}
	public void PrintArray(char[] a) {
		for (int i=0;i<a.length;i++) {
			System.out.println(" "+a[i]);
		}
	}
	public void PrintArray(double[] a) {
		for (int i=0;i<a.length;i++) {
			System.out.println(" "+a[i]);
		}
		System.out.println();
	}

}
